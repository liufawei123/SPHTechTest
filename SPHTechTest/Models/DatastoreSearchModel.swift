import Foundation

struct DatastoreSearch: Codable {
    let help: String
    let success: Bool
    let result: Result
}

struct Result: Codable {
    let resourceID: String
    let fields: [Field]
    let records: [Record]
    let links: Links
    let limit, total: Int

    enum CodingKeys: String, CodingKey {
        case resourceID = "resource_id"
        case fields, records
        case links = "_links"
        case limit, total
    }
}

struct Field: Codable {
    let type, id: String
}

struct Links: Codable {
    let start, next: String
}

struct Record: Codable {
    let volumeOfMobileData, quarter: String
    let id: Int

    enum CodingKeys: String, CodingKey {
        case volumeOfMobileData = "volume_of_mobile_data"
        case quarter
        case id = "_id"
    }
}

// MARK: Convenience initializers

extension DatastoreSearch {
    init?(data: Data) {
        guard let me = try? JSONDecoder().decode(DatastoreSearch.self, from: data) else { return nil }
        self = me
    }

    init?(_ json: String, using encoding: String.Encoding = .utf8) {
        guard let data = json.data(using: encoding) else { return nil }
        self.init(data: data)
    }

    init?(fromURL url: String) {
        guard let url = URL(string: url) else { return nil }
        guard let data = try? Data(contentsOf: url) else { return nil }
        self.init(data: data)
    }

    var jsonData: Data? {
        return try? JSONEncoder().encode(self)
    }

    var json: String? {
        guard let data = self.jsonData else { return nil }
        return String(data: data, encoding: .utf8)
    }
}

